# INTEGER
num = 7
print("integer value is: ", num)
print('its type is', type(num))

# FLOAT
num1 = 7.0
print("Float value is: ", num1)
print('its type is', type(num1))

# STRING
name = 'kARTHI'
print("String value is: ", name)
print('its type is', type(name))

# BOOLEAN
a = 200
b = 33
if b > a:
    print(True)
else:
    print(False)

# LIST
lst = ["KJ", "JK", "LL"]
print(lst)
print('its type is', type(lst))

# TUPLE

# Tuple having integers
my_tuple = (1, 2, 3)
print(my_tuple)

# tuple with mixed datatypes
my_tuple = (1, 'Hello', 3.4)
print(my_tuple)

# nested tuple
my_tuple = ("Hello", [8, 4, 6], (1, 2, 3))
print(my_tuple)

# DICTIONARY
Fruits = {
    'Apple': '100',
    'Orange': '150',
    'Mango': '70'
}
print(Fruits)
